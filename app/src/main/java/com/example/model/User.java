package com.example.model;

public class User {
    public String id;
    public String phone;
    public String name;
    public String username;
    public String password;

    public User(String id, String phone, String name, String username, String password) {
        this.id = id;
        this.phone = phone;
        this.name = name;
        this.username = username;
        this.password = password;
    }
}
